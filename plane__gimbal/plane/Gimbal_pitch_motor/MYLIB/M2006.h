#ifndef __M2006_H__
#define __M2006_H__

#include "config.h"

/**
  * @note  
  * ID 设置的范围为1-8
  * 发送的标识符 0x200 ---对应id为 1-4
  * 输出值范围为 -10000~10000
  * DATA[0] id-1 高8位
  * DATA[1] id-1 低8位
  * DATA[2] id-2 高8位
  * DATA[3] id-2 低8位
  * DATA[4] id-3 高8位
  * DATA[5] id-3 低8位
  * DATA[6] id-4 高8位
  * DATA[7] id-4 低8位
  * 发送的标识符 0x1ff ---对应id为 5-8
  * DATA[0] id-5 高8位  
  * DATA[1] id-5 低8位
  * DATA[2] id-6 高8位
  * DATA[3] id-6 低8位
  * DATA[4] id-7 高8位
  * DATA[5] id-7 低8位
  * DATA[6] id-8 高8位
  * DATA[7] id-8 低8位
  * 反馈id 0x20+id(如id为1，标识符为 0x201)
  * 反馈内容
  * DATA[0] 转子机械角度高8位
  * DATA[1] 转子机械角度低8位
  * DATA[2] 转子转速高8位
  * DATA[3] 转子转速低8位
  * DATA[4] 实际输出转矩高8位
  * DATA[5] 实际输出转矩低8位
  * 发送频率 1kHZ
  * 转子机械角度范围 0~8191
  * 减速比 19:1
  */
  
struct M2006
{
	uint8_t id;
	uint32_t sendId;
	uint32_t receiveId;
	uint8_t mode;

	int16_t sendData;
	uint16_t receiveAngle; //0-8191
	float Angle_zeroCheck;
	int16_t receiveSpeed;
	int16_t qorque;//力矩
	
	float setPoint;
	
	pid_t speedPID;
	pid_t positionPID;
	pid_t qorquePID;
	
	FuzzyPID speedPID_Fuzzy;
	FuzzyPID positionPID_Fuzzy;
	
	ZeroCheck_Typedef zero;
	uint32_t updateTime;
	uint8_t Ever_Lost;
};

#endif // __M2006_H__












